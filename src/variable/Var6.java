package variable;

public class Var6 {
    public  static void main(String[] args){
        char a = 'b';
        String b = "Hello World!";
        System.out.println(a);
        System.out.println(b);
        System.out.println(b);


        // 정수 4가지
        byte c = 123; // -128 ~ 128
        short d = 32767; // -32768 ~ 32767
        int e = 21405; // 대략 20억 정도 사용 가능
        long f = 900000000L; // 겁나 많음, 대문자 L 사용 권장

        // 실수
        float g = 21.3f;  // 소문자 f 이용
        double h = 10.0;

    }
}
